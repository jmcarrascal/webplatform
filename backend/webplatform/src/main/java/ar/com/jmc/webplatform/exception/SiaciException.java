package ar.com.jmc.webplatform.exception;


public class SiaciException extends Exception {

	private static final long serialVersionUID = -2981802769506772009L;

	public SiaciException() {
		super();
	}

	public SiaciException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public SiaciException(String arg0) {
		super(arg0);
	}

	public SiaciException(Throwable arg0) {
		super(arg0);
	}

}
